export default class Vector2 {
    /**
     * X-component of the vector
     * @type {number}
     */
    x;

    /**
     * Y-component of the vector
     * @type {number}
     */
    y;

    /**
     * @param {number} x 
     * @param {number} y 
     */
    constructor(x, y) {
        this.x = x;
        this.y = y;
    }
}
